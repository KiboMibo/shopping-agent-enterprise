    rows = re.findall(r"^\|\s*([1-5])\s*\|", table, flags=re.MULTILINE)
    if rows != ["1", "2", "3", "4", "5"]:
        raise RuntimeError(
            "Шаблон сравнительной таблицы не содержит ровно пять "
            "пронумерованных предложений"
        )


